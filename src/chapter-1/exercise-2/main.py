# Arithmetic

def main():
    a = int(input())
    b = int(input())

    print(f'{a} + {b} is {a + b}')
    print(f'{a} - {b} is {a - b}')
    print(f'{a} * {b} is {a * b}')
    print(f'{a} / {b} is {a / b}')
    print(f'{a} % {b} is {a % b}')
    print(f'{a} ^ {b} is {a ** b}')

if __name__ == "__main__":
    main()